    /**
     * This button lets the user add a town to his own route and print it in a label
     * Also checks that the town added is not the same than previous, and alerts the user about that
     * 
     * @param evt button action performed
     */
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if (path.isEmpty()) {
            path.add(townCombo.getSelectedItem().toString());
            routeLbl.setText(path.get(0));
        } else {
            add = true;
            route = "";
            for (int i = 0; i < path.size(); i++) {
                if (path.get(path.size() - 1).equals(townCombo.getSelectedItem().toString())) {
                    JOptionPane.showMessageDialog(null, "You can not introduce again the same town.");
                    add = false;
                    break;
                }
            }
            if (add) {
                path.add(townCombo.getSelectedItem().toString());
            }
            for (int i = 0; i < path.size(); i++) {
                route += path.get(i) + " ";
            }
            routeLbl.setText(route);
        }
    }//GEN-LAST:event_addBtnActionPerformed

