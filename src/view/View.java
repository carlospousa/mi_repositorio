    /**
     * calls the method that checks every town is or is not connected, get the result and set it in a label
     * 
     * @param evt  button action performed
     */
    private void checkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBtnActionPerformed
        // TODO add your handling code here:
        Graph graph = Controller.generateGraph();

        List<Town> pathTowns = Controller.convertToTown(path, graph);

        distanceLbl.setText(Controller.shortestRoute(pathTowns));
    }//GEN-LAST:event_checkBtnActionPerformed

